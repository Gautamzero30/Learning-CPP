#include <iostream>
using namespace std;

int main() {
    // Declaring variables
    int a, b, sum;

    // Taking input
    cout << "Enter first number: ";
    cin >> a;

    cout << "Enter second number: ";
    cin >> b;

    // Performing addition
    sum = a + b;

    // Displaying output
    cout << "Sum of " << a << " and " << b << " is: " << sum << endl;

    return 0;
}
